package org.webshop.search.model;

public class InputSearch {
	
	private String typeId;
	private String typeName;
	
	private String screenSizeId;
	private String screenSizeName;
	
	private String companyId;
	private String companyName;
	
	public String getTypeId() {
		return typeId;
	}
	
	public void setTypeId(String typeId) {
		this.typeId = typeId;
	}
	
	public String getTypeName() {
		return typeName;
	}
	
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public String getScreenSizeId() {
		return screenSizeId;
	}

	public void setScreenSizeId(String screenSizeId) {
		this.screenSizeId = screenSizeId;
	}

	public String getScreenSizeName() {
		return screenSizeName;
	}

	public void setScreenSizeName(String screenSizeName) {
		this.screenSizeName = screenSizeName;
	}

	public String getCompanyId() {
		return companyId;
	}

	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	
	

}
