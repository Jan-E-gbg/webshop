package org.webshop.search.model;

public class ScreenSize {
	
	private String screenSizeId;
	private String screenSizeName;
	
	
	public void setScreenSizeId(String modell_size_id){
		this.screenSizeId = modell_size_id;
	}
	
	public void setScreenSizeName(String modell_size_name){
		this.screenSizeName = modell_size_name;
	}
	
	public String getScreenSizeId(){
		
		return this.screenSizeId;
	}
	
	public String getScreenSizeName(){
		
		return this.screenSizeName;
	}
	
	
	
}
